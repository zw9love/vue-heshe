</template>
<script>
    import $ from 'jquery'
    import Mynav from './myNav.vue'
    export default{
        components:{
            Mynav
        },
        methods:{
            //点击左上角信息按钮
            infoClick(){
                this.$parent.isGoContain=true;
                this.$parent.isGoShadow=true;
                this.$parent.$children[0].isactive=true;
                this.window.on('touchmove',function(e){e.preventDefault()});
            }
        },
        mounted(){
            if(this.$route.params.back){
                this.infoClick();
            }
        }
    }
</script>
